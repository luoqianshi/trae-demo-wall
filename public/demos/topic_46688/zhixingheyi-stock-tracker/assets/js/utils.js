  // === UUID 生成 ===
  function generateId(prefix) {
    prefix = prefix || 'trx';
    var chars = 'abcdefghijklmnopqrstuvwxyz0123456789';
    var id = '';
    for (var i = 0; i < 8; i++) {
      id += chars.charAt(Math.floor(Math.random() * chars.length));
    }
    return prefix + '_' + id;
  }

  // === 日期格式化 ===
  function formatDate(date, format) {
    if (typeof date === 'string') date = new Date(date);
    if (!(date instanceof Date) || isNaN(date.getTime())) return '';
    format = format || 'YYYY-MM-DD';
    var y = date.getFullYear();
    var m = ('0' + (date.getMonth() + 1)).slice(-2);
    var d = ('0' + date.getDate()).slice(-2);
    var h = ('0' + date.getHours()).slice(-2);
    var min = ('0' + date.getMinutes()).slice(-2);
    var s = ('0' + date.getSeconds()).slice(-2);
    return format
      .replace('YYYY', y).replace('MM', m).replace('DD', d)
      .replace('HH', h).replace('mm', min).replace('ss', s);
  }

  // === 金额格式化 ===
  function formatMoney(amount, showSign) {
    if (amount === null || amount === undefined || isNaN(amount)) return '¥0';
    var sign = showSign ? (amount > 0 ? '+' : '') : '';
    var abs = Math.abs(amount);
    var formatted = abs >= 10000
      ? (abs / 10000).toFixed(2) + '万'
      : abs.toLocaleString('zh-CN', { minimumFractionDigits: 0, maximumFractionDigits: 2 });
    return sign + '¥' + formatted;
  }

  // === 百分比格式化 ===
  function formatPercent(value, showSign) {
    if (value === null || value === undefined || isNaN(value)) return '0%';
    var sign = showSign ? (value > 0 ? '+' : '') : '';
    return sign + value.toFixed(1) + '%';
  }

  // === 获取股票名称（优先查用户缓存，再查STOCK_MAP） ===
  function getStockName(code) {
    return getUserStockName(code) || STOCK_MAP[code] || '';
  }

  // === 搜索股票（模糊匹配代码或名称） ===
  function searchStocks(query, limit) {
    limit = limit || 10;
    if (!query || query.length < 1) return [];
    query = query.toLowerCase();
    var results = [];
    var qLen = query.length;

    // 先合并 STOCK_MAP 和用户自定义缓存
    var allMap = {};
    var code, name;
    for (code in STOCK_MAP) {
      if (STOCK_MAP.hasOwnProperty(code)) allMap[code] = STOCK_MAP[code];
    }
    try {
      var custom = JSON.parse(localStorage.getItem(CUSTOM_STOCK_KEY) || '{}');
      for (code in custom) {
        if (custom.hasOwnProperty(code)) allMap[code] = custom[code];
      }
    } catch (e) {}

    for (code in allMap) {
      if (!allMap.hasOwnProperty(code)) continue;
      name = allMap[code];
      var nameLower = name.toLowerCase();

      // 优先：代码前缀匹配
      if (code.indexOf(query) === 0) {
        results.push({ code: code, name: name, score: 100 - code.length });
      }
      // 名称包含匹配
      else if (nameLower.indexOf(query) >= 0) {
        results.push({ code: code, name: name, score: 50 - nameLower.indexOf(query) });
      }
    }

    results.sort(function(a, b) { return b.score - a.score; });
    return results.slice(0, limit);
  }

