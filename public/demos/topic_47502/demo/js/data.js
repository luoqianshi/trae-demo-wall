function getRecommendation(score, rank, province, mode, firstSubject, secondSubjects, cityPref) {
  const results = { chong: [], wen: [], bao: [], special: [] };
  universityData.forEach(uni => {
    if (cityPref && !uni.city.includes(cityPref)) return;
    const scoreDiff = score - uni.score2024;
    let tier = '';
    let probability = 0;
    if (scoreDiff >= 0 && scoreDiff < 10) {
      tier = 'chong';
      probability = 30 + scoreDiff * 5;
    } else if (scoreDiff >= 10 && scoreDiff < 25) {
      tier = 'wen';
      probability = 60 + (scoreDiff - 10) * 2;
    } else if (scoreDiff >= 25) {
      tier = 'bao';
      probability = 90 + Math.min((scoreDiff - 25) * 0.4, 9);
    }
    if (tier) {
      results[tier].push({ ...uni, probability: Math.min(probability, 99) });
    }
    if (uni.features.includes('强基计划') && scoreDiff >= -20) {
      results.special.push({ ...uni, probability: 70, planType: '强基计划' });
    }
  });
  results.chong.sort((a, b) => b.score2024 - a.score2024);
  results.wen.sort((a, b) => b.score2024 - a.score2024);
  results.bao.sort((a, b) => b.score2024 - a.score2024);
  return results;
}

