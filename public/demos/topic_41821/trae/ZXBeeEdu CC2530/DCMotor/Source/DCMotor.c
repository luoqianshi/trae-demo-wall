/*********************************************************************************************
* 头文件
*********************************************************************************************/
#include <ioCC2530.h>
#include "hal_mcu.h"
#include "DCMotor.h"

/*********************************************************************************************
* 宏定义
*********************************************************************************************/
#define CLKDIV  ( CLKCONCMD & 0x07 )    
#define DC_MOTOR_SBIT1  P0_1
#define DC_MOTOR_SBIT2  P0_5
#define DC_MOTOR_SEL   P0SEL
#define DC_MOTOR_DIR   P0DIR
/*********************************************************************************************
* 全局变量
*********************************************************************************************/

/*********************************************************************************************
* 名称：dc_motor_init()
* 功能：直流电机初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void dc_motor_init(void)
{
  P0SEL &= ~0x22;                                               // 设置P0_1,P0_5为普通I/O口
  P0DIR |= 0x22;                                                // 设置P0_1,P0_5为输出
  
  dc_motor_stop();
}

/*********************************************************************************************
* 名称：dc_motor_work()
* 功能：直流电机工作控制
* 参数：cmd
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void dc_motor_work(int8 cmd)
{  
  if (cmd & 0x01 == 0x01)
  {
    dc_motor_clockwise();
  }
  else if (cmd & 0x11 == 0x11)
  {
    dc_motor_contrarotate();
  }
  else
  {
    dc_motor_stop();
  } 
}

/*********************************************************************************************
* 名称：dc_motor_clockwise()
* 功能：直流电机顺时针转动
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void dc_motor_clockwise(void)
{
  DC_MOTOR_SBIT1 = 0;
  DC_MOTOR_SBIT2 = 1;
}

/*********************************************************************************************
* 名称：dc_motor_clockwise()
* 功能：直流电机逆时针转动
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void dc_motor_contrarotate(void)
{
  DC_MOTOR_SBIT1 = 1;
  DC_MOTOR_SBIT2 = 0;
}

/*********************************************************************************************
* 名称：dc_motor_stop()
* 功能：直流电机停止转动
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void dc_motor_stop(void)
{
  DC_MOTOR_SBIT1 = 0;
  DC_MOTOR_SBIT2 = 0;
}
