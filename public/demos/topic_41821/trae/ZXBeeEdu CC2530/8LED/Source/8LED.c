#define CLKDIV  ( CLKCONCMD & 0x07 )    
#define SRCK         P1_1   // 01版本底板为P1_1，02版本底板为P1_0，注意与gpio_init()统一
#define RCK          P0_4
#define G            P1_3
/*********************************************************************************************
* 全局变量
*********************************************************************************************/


/*********************************************************************************************
* 名称：_8LED_init()
* 功能：8位数码管初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/

/*SPI IO模拟函数
-------------------------------------------------------*/
void write_595(unsigned char byte)
{
  int i;
  RCK = 0;                   //先拉低，为后面的上升沿做准备
  for(i=0; i<8; i++)
  {
    SRCK = 0;               //先拉低，为后面的上升沿做准备
    if(byte & 0x80) G = 1;  //将字节最高位 输出在MOSI线上
    else G = 0;
    byte = byte << 1;       //该字节右移一位
    SRCK = 1;               //使74HC595接收该位
  }
  RCK = 1;                  //使74HC595输出该字节
}
