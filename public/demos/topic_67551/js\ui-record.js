function generateXunfeiAuthUrl(config) {
  return new Promise((resolve) => {
    const host = 'iat-api.xfyun.cn';
    const path = '/v2/iat';
    const date = new Date().toUTCString();
    
    const signatureOrigin = `host: ${host}\ndate: ${date}\nGET ${path} HTTP/1.1`;
    
    console.log('=== 讯飞鉴权调试信息 ===');
    console.log('host:', host);
    console.log('path:', path);
    console.log('date:', date);
    console.log('签名原始字符串:', JSON.stringify(signatureOrigin));
    console.log('apiKey:', config.apiKey);
    console.log('apiSecret 长度:', config.apiSecret.length);
    
    const cryptoObj = window.crypto || window.msCrypto;
    if (!cryptoObj || !cryptoObj.subtle) {
      console.error('浏览器不支持 WebCrypto API，无法生成签名');
      resolve(null);
      return;
    }
    
    const encoder = new TextEncoder();
    const keyData = encoder.encode(config.apiSecret);
    const messageData = encoder.encode(signatureOrigin);
    
    console.log('keyData 字节数:', keyData.length);
    console.log('messageData 字节数:', messageData.length);
    
    cryptoObj.subtle.importKey(
      'raw', keyData, { name: 'HMAC', hash: 'SHA-256' }, false, ['sign']
    ).then(key => {
      return cryptoObj.subtle.sign('HMAC', key, messageData);
    }).then(signature => {
      const signatureBytes = new Uint8Array(signature);
      console.log('签名结果字节数:', signatureBytes.length);
      
      // 正确的 base64 编码方式
      let binary = '';
      for (let i = 0; i < signatureBytes.length; i++) {
        binary += String.fromCharCode(signatureBytes[i]);
      }
      const signatureBase64 = btoa(binary);
      
      console.log('签名 base64:', signatureBase64);
      
      const authOrigin = `api_key="${config.apiKey}", algorithm="hmac-sha256", headers="host date request-line", signature="${signatureBase64}"`;
      console.log('authorization 原始字符串:', authOrigin);
      
      const authorization = btoa(authOrigin);
      console.log('authorization base64:', authorization);
      
      const url = `wss://${host}${path}?authorization=${authorization}&date=${encodeURIComponent(date)}&appid=${config.appId}`;
      console.log('最终 URL 前100字符:', url.substring(0, 100));
      console.log('========================');
      
      resolve(url);
    }).catch((err) => {
      console.error('签名生成失败:', err);
      resolve(null);
    });
  });
}

function floatTo16BitPCM(input) {
  const output = new Int16Array(input.length);
  for (let i = 0; i < input.length; i++) {
    const s = Math.max(-1, Math.min(1, input[i]));
    output[i] = s < 0 ? s * 0x8000 : s * 0x7FFF;
  }
  return output;
}

function arrayBufferToBase64(buffer) {
  let binary = '';
  const bytes = new Uint8Array(buffer);
  const chunkSize = 0x8000;
  for (let i = 0; i < bytes.length; i += chunkSize) {
    const chunk = bytes.subarray(i, i + chunkSize);
    binary += String.fromCharCode.apply(null, chunk);
  }
  return btoa(binary);
}

function resampleTo16k(inputData, inputSampleRate, targetSampleRate) {
  if (inputSampleRate === targetSampleRate) return inputData;
  
  const ratio = inputSampleRate / targetSampleRate;
  const newLength = Math.floor(inputData.length / ratio);
  const result = new Float32Array(newLength);
  
  for (let i = 0; i < newLength; i++) {
    const idx = Math.floor(i * ratio);
    result[i] = inputData[idx];
  }
  
  return result;
}

async function startXunfeiRecognition() {
  const config = getXunfeiConfig();
  if (!config.appId || !config.apiKey || !config.apiSecret) {
    showToast('请先配置讯飞语音识别 API');
    openXunfeiConfigModal();
    return;
  }
  
  if (xunfeiIsRecording) {
    stopXunfeiRecognition();
    return;
  }
  
  try {
    xunfeiMediaStream = await navigator.mediaDevices.getUserMedia({ 
      audio: {
        channelCount: 1,
        echoCancellation: true,
        noiseSuppression: true,
        autoGainControl: true
      }
    });
    
    xunfeiAudioContext = new (window.AudioContext || window.webkitAudioContext)();
    const source = xunfeiAudioContext.createMediaStreamSource(xunfeiMediaStream);
    
    const bufferSize = 4096;
    xunfeiScriptProcessor = xunfeiAudioContext.createScriptProcessor(bufferSize, 1, 1);
    source.connect(xunfeiScriptProcessor);
    xunfeiScriptProcessor.connect(xunfeiAudioContext.destination);
    
    const authUrl = await generateXunfeiAuthUrl(config);
    if (!authUrl) {
      showToast('鉴权URL生成失败，请检查API配置');
      stopXunfeiRecognition();
      return;
    }
    
    console.log('讯飞配置:', { appId: config.appId, apiKeyLen: config.apiKey.length, apiSecretLen: config.apiSecret.length });
    console.log('正在连接讯飞 WebSocket...', authUrl.substring(0, 50) + '...');
    xunfeiWS = new WebSocket(authUrl);
    
    let connectionTimeout = setTimeout(() => {
      if (xunfeiWS && xunfeiWS.readyState === 0) {
        console.error('讯飞WebSocket连接超时 (8秒未响应)');
        showToast('语音识别连接超时，请检查网络');
        stopXunfeiRecognition();
      }
    }, 8000);
    
    xunfeiWS.onopen = () => {
      clearTimeout(connectionTimeout);
      console.log('✅ 讯飞 WebSocket 连接成功');
      xunfeiFinalText = '';
      xunfeiIsRecording = true;
      xunfeiFirstFrameSent = false;
      updateVoiceRecorderUI(true);
      
      const realtimeTextEl = document.getElementById('voice-realtime-text');
      if (realtimeTextEl) {
        realtimeTextEl.textContent = '正在聆听...请说话';
        realtimeTextEl.style.color = 'var(--primary)';
      }
    };
    
    xunfeiWS.onmessage = (event) => {
      try {
        const data = JSON.parse(event.data);
        console.log('讯飞返回:', data);
        
        if (data.code !== 0) {
          console.error('❌ 讯飞识别错误 code:', data.code, 'message:', data.message, 'sid:', data.sid);
          const errorMsg = data.message || ('错误码: ' + data.code);
          showToast('识别失败: ' + errorMsg);
          stopXunfeiRecognition();
          return;
        }
        
        if (data.data && data.data.result) {
          const ws = data.data.result.ws;
          let text = '';
          ws.forEach(item => {
            item.cw.forEach(cw => {
              text += cw.w;
            });
          });
          
          if (data.data.result.pgs === 'rpl') {
            const rn = data.data.result.rn || 0;
            const chars = xunfeiFinalText.split('');
            xunfeiFinalText = chars.slice(0, Math.max(0, chars.length - rn)).join('') + text;
          } else {
            xunfeiFinalText += text;
          }
          
          const realtimeTextEl = document.getElementById('voice-realtime-text');
          if (realtimeTextEl) {
            realtimeTextEl.textContent = xunfeiFinalText;
            realtimeTextEl.style.color = 'var(--text-primary)';
          }
        }
      } catch (e) {
        console.error('解析讯飞返回失败:', e, '原始数据:', event.data);
      }
    };
    
    xunfeiWS.onerror = (event) => {
      clearTimeout(connectionTimeout);
      console.error('❌ 讯飞 WebSocket 错误事件:', event);
      console.error('WebSocket readyState:', xunfeiWS ? xunfeiWS.readyState : 'null');
      
      let errorMsg = '语音识别连接失败';
      if (xunfeiWS && xunfeiWS.readyState === 3) {
        errorMsg = '连接被关闭，请检查API配置或网络';
      }
      showToast(errorMsg);
      xunfeiIsRecording = false;
      updateVoiceRecorderUI(false);
    };
    
    xunfeiWS.onclose = (event) => {
      clearTimeout(connectionTimeout);
      console.log('🔌 讯飞 WebSocket 关闭:', event.code, event.reason);
      xunfeiIsRecording = false;
      updateVoiceRecorderUI(false);
    };
    
    xunfeiScriptProcessor.onaudioprocess = (e) => {
      if (!xunfeiIsRecording || !xunfeiWS || xunfeiWS.readyState !== 1) return;
      
      const inputBuffer = e.inputBuffer;
      const inputData = inputBuffer.getChannelData(0);
      
      const sampleRate = inputBuffer.sampleRate;
      const resampled = resampleTo16k(inputData, sampleRate, 16000);
      const pcmData = floatTo16BitPCM(resampled);
      const base64Data = arrayBufferToBase64(pcmData.buffer);
      
      let frame;
      if (!xunfeiFirstFrameSent) {
        xunfeiFirstFrameSent = true;
        frame = {
          common: {
            app_id: config.appId
          },
          business: {
            language: 'zh_cn',
            domain: 'iat',
            accent: 'mandarin',
            vad_eos: 5000,
            punc: 1
          },
          data: {
            status: 0,
            format: 'audio/L16;rate=16000',
            audio: base64Data,
            encoding: 'raw'
          }
        };
      } else {
        frame = {
          data: {
            status: 1,
            format: 'audio/L16;rate=16000',
            audio: base64Data,
            encoding: 'raw'
          }
        };
      }
      
      try {
        xunfeiWS.send(JSON.stringify(frame));
      } catch (e) {
        console.error('发送音频失败:', e);
      }
    };
    
  } catch (error) {
    console.error('启动录音失败:', error);
    if (error.name === 'NotAllowedError') {
      showToast('麦克风权限被拒绝，请在浏览器设置中允许');
    } else if (error.name === 'NotFoundError') {
      showToast('未找到麦克风设备');
    } else {
      showToast('无法访问麦克风: ' + error.message);
    }
  }
}

function stopXunfeiRecognition() {
  xunfeiIsRecording = false;
  
  if (xunfeiWS && xunfeiWS.readyState === 1) {
    try {
      const endFrame = {
        data: {
          status: 2,
          format: 'audio/L16;rate=16000',
          audio: '',
          encoding: 'raw'
        }
      };
      xunfeiWS.send(JSON.stringify(endFrame));
    } catch (e) {}
  }
  
  if (xunfeiScriptProcessor) {
    try { xunfeiScriptProcessor.disconnect(); } catch(e) {}
    xunfeiScriptProcessor = null;
  }
  if (xunfeiAudioContext) {
    try { xunfeiAudioContext.close(); } catch(e) {}
    xunfeiAudioContext = null;
  }
  if (xunfeiMediaStream) {
    xunfeiMediaStream.getTracks().forEach(track => track.stop());
    xunfeiMediaStream = null;
  }
  if (xunfeiWS) {
    setTimeout(() => {
      if (xunfeiWS) {
        try { xunfeiWS.close(); } catch(e) {}
        xunfeiWS = null;
      }
    }, 1000);
  }
  
  updateVoiceRecorderUI(false);
}

