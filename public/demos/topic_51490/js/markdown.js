  function searchGuides(query) {
    const q = query.toLowerCase();
    const results = [];
    Object.entries(guides).forEach(([id, guide]) => {
      const content = guide.content.toLowerCase();
      const title = guide.title.toLowerCase();
      const tags = guide.tags.join(' ').toLowerCase();
      if (title.includes(q) || tags.includes(q) || content.includes(q)) {
        const matchIndex = content.indexOf(q);
        const snippet = matchIndex > -1
          ? content.substring(Math.max(0, matchIndex - 30), matchIndex + 80) + '...'
          : title;
        results.push({
          id,
          title: guide.title,
          icon: guide.icon,
          snippet
        });
      }
    });
    return results;
  }

