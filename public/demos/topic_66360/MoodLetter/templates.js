function getRandomItem(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

function determineIntensity(text) {
    const severeWords = ['绝望', '想死', '活不下去', '不想活', '自杀', '没用', '一无是处', '崩溃', '撑不下去'];
    const moderateWords = ['很累', '难受', '委屈', '害怕', '焦虑', '紧张', '压力大', '不想去', '睡不着'];
    
    if (severeWords.some(word => text.includes(word))) {
        return 'severe';
    } else if (moderateWords.some(word => text.includes(word)) || text.length > 50) {
        return 'moderate';
    } else {
        return 'mild';
    }
}

