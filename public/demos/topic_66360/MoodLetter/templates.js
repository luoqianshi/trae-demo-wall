const attackWords = [
    '去死吧', '滚', '混蛋', '去死', '你去死', '让他去死'
];

function checkRiskLevel(text) {
    const trimmedText = text.trim();
    
    if (attackWords.some(word => trimmedText.includes(word))) {
        if (trimmedText.length <= 5 || trimmedText.startsWith('你') || trimmedText.startsWith('让')) {
            return riskLevels.NONE;
        }
    }
    
    let level = riskLevels.NONE;
    
    if (plannedRiskWords.some(word => trimmedText.includes(word)) || 
        plannedRiskPatterns.some(pattern => pattern.test(trimmedText))) {
        level = riskLevels.PLANNED;
    } else if (clearRiskWords.some(word => trimmedText.includes(word)) || 
               clearRiskPatterns.some(pattern => pattern.test(trimmedText))) {
        level = riskLevels.CLEAR;
    } else if (suspectedRiskWords.some(word => trimmedText.includes(word)) || 
               suspectedRiskPatterns.some(pattern => pattern.test(trimmedText))) {
        level = riskLevels.SUSPECTED;
    }
    
    return level;
}

function checkHighRisk(text) {
    return checkRiskLevel(text) >= riskLevels.SUSPECTED;
}

