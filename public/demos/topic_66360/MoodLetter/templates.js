function generateChildResult(text, emotion) {
    const scene = detectChildScene(text);
    const templates = childTemplates[scene];
    const quotes = famousQuotes[scene];
    
    const intensity = determineIntensity(text);
    const expressList = templates.express;
    
    let express;
    if (typeof expressList === 'object' && expressList.mild) {
        const availableLevels = Object.keys(expressList);
        if (availableLevels.includes(intensity)) {
            express = getRandomItem(expressList[intensity]);
        } else {
            const levelKeys = ['mild', 'moderate', 'severe'].filter(k => availableLevels.includes(k));
            express = getRandomItem(expressList[levelKeys[levelKeys.length - 1]]);
        }
    } else {
        express = getRandomItem(expressList);
    }
    
    const modifier = emotionModifiers[emotion] || emotionModifiers.sad;
    
    let wish = getRandomItem(templates.wish);
    if (modifier.wishPrefix && !wish.startsWith(modifier.wishPrefix)) {
        wish = modifier.wishPrefix + wish;
    }
    
    let action = getRandomItem(templates.action);
    if (modifier.actionSuffix && !action.includes(modifier.actionSuffix)) {
        action += ' ' + modifier.actionSuffix;
    }
    
    let quote = getRandomItem(quotes);
    
    const gentle = [...modifier.gentle, ...templates.gentle];
    
    return {
        express: express,
        wish: wish,
        letter: getRandomItem(templates.letter),
        action: action,
        quote: quote,
        gentle: [getRandomItem(gentle), getRandomItem(gentle)].filter((item, index, self) => self.indexOf(item) === index)
    };
}

function generateParentResult(text) {
    const scene = detectParentScene(text);
    const templates = parentTemplates[scene];
    const quotes = parentQuotes[scene];
    
    const dontSayIndex = Math.floor(Math.random() * templates.dontSay.length);
    
    return {
        need: getRandomItem(templates.need),
        dontSay: templates.dontSay[dontSayIndex],
        dontSayWhy: templates.dontSayWhy ? templates.dontSayWhy[dontSayIndex] : '',
        response: getRandomItem(templates.response),
        action: getRandomItem(templates.action),
        boundary: templates.boundary ? templates.boundary.join('<br>') : '',
        quote: getRandomItem(quotes)
    };
}
