export function generateDemoReply(input: string, context?: DemoReplyContext): string {
  const lower = input.toLowerCase()
  const scenario = SCENARIOS.find(s => s.keywords.some(k => lower.includes(k.toLowerCase())))
  const ctx = context || {}
  const body = scenario ? scenario.reply(ctx) :
    `我已经记住你刚刚说的内容。为了给你更有针对性的建议，可以继续补充背景，或在设置中配置 API Key，让我调用多 Agent 团队为你做全维度分析。`
  return `【示例回复】${body}\n\n---\n💡 当前为离线体验模式。配置 API Key 后，衡舟会调用真实模型并结合你的人物档案、历史记忆给出个性化回复。`
}

export function generateDemoProfile(input: string): string {
  const lower = input.toLowerCase()
  const parts: string[] = []

  if (lower.includes('陈志远')) parts.push('姓名：陈志远')
  else if (/我叫(\S+)/.test(input)) {
    const m = input.match(/我叫(\S+)/)
    if (m) parts.push(`姓名：${m[1]}`)
  }

  if (/\b(\d{2})\s*岁/.test(input)) {
    const m = input.match(/\b(\d{2})\s*岁/)
    if (m) parts.push(`年龄：${m[1]}岁`)
  } else if (lower.includes('42')) {
    parts.push('年龄：42岁')
  }

  if (lower.includes('杭州')) parts.push('城市：杭州')
  if (lower.includes('总监') || lower.includes('经理')) parts.push('职业：企业中层管理者')
  if (lower.includes('妻子') || lower.includes('孩子') || lower.includes('妈妈')) parts.push('家庭：有家人需要照顾')
  if (lower.includes('压力') || lower.includes('焦虑') || lower.includes('失眠')) parts.push('当前状态：面临较大压力')

  if (parts.length === 0) {
    return '目前了解还不够多，你可以继续聊聊自己的工作、家庭或最近的状态。'
  }

  return parts.join('，') + '。'
}
