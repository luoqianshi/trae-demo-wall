export async function seedDemoData(onProgress?: (current: number, total: number) => void) {
  // 防止并发播种（HMR 重载时可能触发多次）
  const SEEDING_KEY = 'hengzhou-seeding-in-progress'
  if (localStorage.getItem(SEEDING_KEY)) {
    console.log('[Demo Seed] 检测到正在播种中，跳过')
    return
  }
  localStorage.setItem(SEEDING_KEY, '1')

  try {
    // 给所有示例对象加 demo 标记
    PEOPLE.forEach((p) => { p.isDemo = 1 })
    MEMORIES.forEach((m) => { m.isDemo = 1 })
    DIARIES.forEach((d) => { d.isDemo = 1 })

    // 使用事务确保原子性：清空+写入要么全部成功，要么全部回滚
    // 防止 HMR 重载导致清空后写入失败、数据库变空
    await db.transaction('rw', [db.persons, db.interactions, db.memories, db.diaries, db.conversations, db.embeddings, db.agentTasks, db.knowledgeGraph], async () => {
      await db.persons.clear()
      await db.interactions.clear()
      await db.memories.clear()
      await db.diaries.clear()
      await db.conversations.clear()
      await db.embeddings.clear()
      await db.agentTasks.clear()
      await db.knowledgeGraph.clear()

      // 写入人物
      await db.persons.bulkAdd(PEOPLE)

      // 写入记忆
      await db.memories.bulkAdd(MEMORIES)

      // 写入日记
      await db.diaries.bulkAdd(DIARIES)
    })

    console.log('[Demo Seed] 陈志远的企业级个人信息库已初始化')
    console.log(`  - ${PEOPLE.length} 个丰满人物档案（含多维度画像+时间线+关系图谱）`)
    console.log(`  - ${MEMORIES.length} 条关联记忆`)
    console.log(`  - ${DIARIES.length} 篇分析日记`)
    console.log(`  - ${STRESS_TEST_SCENARIOS.length} 个高压测试场景`)

    // 自动建立向量索引，确保语义检索可用
    try {
      await rebuildIndex(onProgress)
      console.log('[Demo Seed] 向量索引已自动重建')
    } catch (e) {
      console.warn('[Demo Seed] 向量索引重建失败:', e instanceof Error ? e.message : String(e))
    }
  } finally {
    localStorage.removeItem(SEEDING_KEY)
  }
}

