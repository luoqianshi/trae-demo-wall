/**
 * 校验数据库关键表是否有数据，防止极端情况下数据不完整
 */
export async function validateDataIntegrity(): Promise<{ ok: boolean; missing: string[] }> {
  const checks = [
    { table: 'persons', min: 1 },
    { table: 'memories', min: 1 },
  ]
  const missing: string[] = []
  for (const { table, min } of checks) {
    const count = await (db as any)[table].count()
    if (count < min) missing.push(`${table}(${count}/${min})`)
  }
  return { ok: missing.length === 0, missing }
}

