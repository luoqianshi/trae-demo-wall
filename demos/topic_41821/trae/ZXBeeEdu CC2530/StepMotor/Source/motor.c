#include <ioCC2530.h>
#include "hal_mcu.h"
#include "motor.h"

/*********************************************************************************************
* 宏定义
*********************************************************************************************/
#define CLKDIV  ( CLKCONCMD & 0x07 )    
#define AA   P0_5 //P0_0
#define BB   P0_1 //P0_5
#define CC   P0_6
#define DD   P1_3 //P0_7

/*********************************************************************************************
* 全局变量
*********************************************************************************************/
volatile char Flag = 2;

/*********************************************************************************************
* 名称：motor_init()
* 功能：步进电机初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void motor_init(void)
{
  P0SEL &= ~0x62;                                               // 设置P0为普通 I/O 口
  P0DIR |= 0x62;                                                // 设置P0.0为输出
  P1SEL &= ~0x08;                                               // 设置P1为普通 I/O 口
  P1DIR |= 0x08;                                                // 设置P1.2,P1.6,P1.7为输出   
  
  motor_stop();
}

/*********************************************************************************************
* 名称：motor_step()
* 功能：步进电机转动
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
static int8 s = 0;
void motor_step(uint8 cmd)
{

  
  if (cmd > 0) s +=1;
  else s -= 1;
  if (s<0) s = 7;
  if (s>7) s = 0;
  
  switch (s) {
  case 0:
    AA = 1;
    BB = 0;
    CC = 0;
    DD = 0;
    break;
  case 1:
    AA = 1;
    BB = 1;
    CC = 0;
    DD = 0;
    break;
  case 2:
    AA = 0;
    BB = 1;
    CC = 0;
    DD = 0;
    break;
  case 3:
    AA = 0;
    BB = 1;
    CC = 1;
    DD = 0;
    break;
  case 4:
    AA = 0;
    BB = 0;
    CC = 1;
    DD = 0;
    break;
  case 5:
    AA = 0;
    BB = 0;
    CC = 1;
    DD = 1;
    break;
  case 6:
    AA = 0;
    BB = 0;
    CC = 0;
    DD = 1;
    break;
  case 7:
    AA = 1;
    BB = 0;
    CC = 0;
    DD = 1;
    break;
  }
}

/*********************************************************************************************
* 名称：MotorStop()
* 功能：步进电机停止转动
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void motor_stop(void)
{
  AA = 0;
  BB = 0;
  CC = 0;
  DD = 0;
}