export const getNewsByCategory = (category: string): NewsItem[] => {
  if (category === 'all') return newsList;
  return newsList.filter((n) => n.category === category);
};

export const getNewsById = (id: string): NewsItem | undefined => {
  return newsList.find((n) => n.id === id);
};

export const getUrgentNews = (): NewsItem[] => {
  return newsList.filter((n) => n.urgency === 'high');
};

export const getRecentNews = (count: number = 5): NewsItem[] => {
  return [...newsList].sort((a, b) => b.date.localeCompare(a.date)).slice(0, count);
};
