    /**
     * 获取NPC专属开场白（支持好感度维度）
     * @param {string} npcId
     * @param {string} moodLevel - 'low'/'mid'/'high'
     * @param {string} [relation] - 'stranger'/'acquaintance'/'friend'
     * @returns {string|null}
     */
    function getOpening(npcId, moodLevel, relation) {
        var db = getDialogueDb(npcId);
        if (!db || !db.openings || !db.openings[moodLevel]) return null;
        var moodSet = db.openings[moodLevel];
        // 优先使用好感度匹配的开场白
        if (relation && moodSet[relation]) {
            return pick(moodSet[relation]);
        }
        // 回退：合并所有好感度的开场白
        var all = [];
        for (var k in moodSet) {
            if (moodSet.hasOwnProperty(k)) {
                all = all.concat(moodSet[k]);
            }
        }
        return all.length > 0 ? pick(all) : null;
    }

